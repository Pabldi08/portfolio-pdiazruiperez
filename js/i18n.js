let currentLang = localStorage.getItem("pdruiperez-lang") ?? "es";

export function t(key) {
    return translations[currentLang]?.[key] ?? translations.es[key] ?? key;
}

export function getLang() {
    return currentLang;
}

export function setLang(lang) {
    currentLang = lang;
    localStorage.setItem("pdruiperez-lang", lang);
}

export function localizeItem(item) {
    if (currentLang === "es" || !item.en) return item;
    const merged = { ...item, ...item.en };
    if (item.artifact && item.en.artifact) {
        merged.artifact = { ...item.artifact, ...item.en.artifact };
    }
    if (item.link && item.en.link) {
        merged.link = { ...item.link, ...item.en.link };
    }
    return merged;
}

export function applyI18nToStatic() {
    document.documentElement.lang = currentLang;
    document.querySelectorAll("[data-i18n]").forEach(el => {
        el.innerHTML = t(el.dataset.i18n);
    });
}
